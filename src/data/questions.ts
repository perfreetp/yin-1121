export const getQuestionById = (id: string): Question | undefined => {
  return questionList.find(q => q.id === id);
};

export const getQuestionsByKnowledge = (knowledgeId: string): Question[] => {
  return questionList.filter(q => q.knowledgeId === knowledgeId);
};

export const getQuestionsByType = (type: Question['type']): Question[] => {
  return questionList.filter(q => q.type === type);
};

export const getQuestionsByItemType = (itemType: Question['itemType']): Question[] => {
  return questionList.filter(q => q.itemType === itemType);
};

export const getRandomQuestions = (count: number, excludeIds: string[] = []): Question[] => {
  const available = questionList.filter(q => !excludeIds.includes(q.id));
  const shuffled = [...available].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
};
